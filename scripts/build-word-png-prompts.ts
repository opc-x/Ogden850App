function guideHook(word: string): string | undefined {
  const g = (wordGuides as Record<string, { hook?: string; concept?: string }>)[word];
  return g?.hook || g?.concept;
}

function generalSemantic(word: string, cn: string): string {
  const hook = guideHook(word);
  if (hook) return hook.replace(/[【】]/g, '').slice(0, 80);
  return cn || word;
}

function qualitySemantic(word: string, cn: string): string {
  const colorWords = ['red', 'blue', 'green', 'yellow', 'white', 'black', 'brown', 'grey', 'gray'];
  if (colorWords.includes(word)) return `A large ${word} colored circle fill, color quality`;
  const faceWords = ['angry', 'happy', 'sad', 'afraid', 'surprised', 'tired', 'sleepy'];
  if (faceWords.includes(word)) return `A simple face expression showing ${word} emotion, feeling`;
  const hook = guideHook(word);
  if (hook) return `Visual metaphor for ${word} (${cn}): ${hook.slice(0, 60)}`;
  return `Clear visual symbol representing the quality ${word} meaning ${cn}`;
}

function oppositeSemantic(word: string, cn: string): string {
  const g = (wordGuides as Record<string, { opposite?: string }>)[word];
  const pair = g?.opposite;
  if (pair) return `Single clear illustration of ${word} pole contrasted subtly with ${pair}, opposite concept`;
  return `Clear visual for opposite quality ${word} (${cn})`;
}

