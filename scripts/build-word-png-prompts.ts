    const entries = Object.entries(obj)
      .sort(([a], [b]) => a.localeCompare(b))
      .map(([k, v]) => `  ${k}: '${v.replace(/'/g, "\\'")}',`)
      .join('\n');
    return `export const ${name}: Record<string, string> = {\n${entries}\n};`;
