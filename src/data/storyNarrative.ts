export function getStoryNarrative(sceneKey: string): StoryNarrative {
  const explicit = NARRATIVES[sceneKey];
  if (explicit) return explicit;

  const script = getSceneStory(sceneKey);
  if (!script) {
    return {
      when: '今天',
      where: sceneKey,
      who: '甲（学习者）与乙（当地人）',
      how: '面对面交谈',
      method: '用简单英语说明需要、确认、完成',
      event: '完成一件与生活相关的小事',
    };
  }

  return {
    when: '今天（白天或傍晚，符合场景）',
    where: script.titleZh,
    who: `甲（当事人）· 乙（${script.titleZh}场景中的服务员/朋友/工作人员）`,
    how: '自然口语，一问一答',
    method: script.storyHook,
    event: `${script.storyHook}——过程中要遇到一个小状况并解决`,
  };
}

